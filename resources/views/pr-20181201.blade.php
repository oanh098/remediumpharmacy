                                    <p>The new position is being funded by medicinal marijuana bio-tech company Remedium Bio-Technology of Edmonton and the New Brunswick Health Research Foundation. </p>
                                    <p>Dr. Thornton spent the last five years at Brock University in St. Catharines, Ontario researching CBD extracts and developing synthesized versions of chemicals found
                                        in plant for use in chemotherapy. He told the CBC that he will draw on the experience leading that research to study the potential health benefits of marijuana. </p>
                                    <p>"We know some thing about the medicinal value of cannabinoids, about the major two cannabinoids, THC and CBD. The information about other cannabinoids that also accumulate
                                        in the plant are very scarce, so this is an area that we need to study," Dr. Thornton said. </p>
                                    <p>Particular genes in marijuana are responsible for the production of THC, CBD, and other cannabinoids. </p>
                                    <p>"We could transfer these genes from the marijuana plant and into different kind of microorganisms. Potentially you could produce very pure compounds continuing research
                                        into marijuana should lead to a lessening of the stigma still attached to the plant," he added </p>
                                    <p>"In the past, even in Canada, marijuana is considered a taboo but we're seeing the change of image of the plant with legalization. Especially with discovery of medicinal use of the
                                        planet we're going to create a very positive image of the plant." </p>
                                    <p> Dr. Thornton will focus on the medicinal applications of the plant and improving marijuana agriculture. Especially in the beginning of his five-year assignment, he plans to meet
                                        with marijuana producers and government agencies to determine where more research is needed. </p>
                                    <p>"This research would have direct implication with the industry," he said. "I would like to talk with local industry and the private sector as well government agencies of food and
